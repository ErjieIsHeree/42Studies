#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	a;

	if (n == INT_MIN)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n / 10)
		ft_putnbr_fd(n / 10, fd);
	a = '0' + (n % 10);
	write(fd, &a, 1);
}
